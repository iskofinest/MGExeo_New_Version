    private void tblWithdrawalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblWithdrawalMouseClicked
        
        SwingUtilities.invokeLater(() -> {
            if(evt.getClickCount()>1) {
                JTable source = (JTable)evt.getSource();
                int row = source.rowAtPoint( evt.getPoint());
                WithdrawalReport withdrawalReport = withdrawalReports.get(row);
                dispose();
                new ViewWithdrawalReport(withdrawalReport.getId(), this).setVisible(true);
            }
        });
        
    }//GEN-LAST:event_tblWithdrawalMouseClicked

