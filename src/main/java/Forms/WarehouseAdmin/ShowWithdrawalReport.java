    private void btnAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAllActionPerformed
        SwingUtilities.invokeLater(() -> {
            enableFields(!btnAll.isSelected());
            if(btnAll.isSelected()) {
                withdrawalReports = WithdrawalReportService.findAll();
            } else {
                withdrawalReports = new ArrayList();
            }
            reloadTable();
        });
    }//GEN-LAST:event_btnAllActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        Project project = (cbxProject.getSelectedIndex()<0)?null:projects.get(cbxProject.getSelectedIndex());
        User requisitioner = (cbxRequisitioner.getSelectedIndex()<0) ?null :requisitioners.get(cbxRequisitioner.getSelectedIndex());
        Date dateFrom = jdcDateFrom.getDate();
        Date dateTo = jdcDateTo.getDate();
        SwingUtilities.invokeLater(() -> {
            withdrawalReports = WithdrawalReportService.findBy(project, requisitioner, dateFrom, dateTo);
            reloadTable();
        });
    }//GEN-LAST:event_btnSearchActionPerformed

