    private void initializeData() {
        columns.add("Withdrawal #");
        columns.add("Withdrawal Date");
        columns.add("Total Amount");
        columns.add("Project Name");
        columns.add("Requisitioner");
        projects = ProjectService.findAll();
        AutoCompleteDecorator.decorate(cbxProject);
        DefaultComboBoxModel projectModel = new DefaultComboBoxModel();
        projects.forEach(project -> {
            projectModel.addElement(project.getProjectName());
        });
        
        requisitioners = UserService.findAll();
        AutoCompleteDecorator.decorate(cbxRequisitioner);
        DefaultComboBoxModel requisitionerModel = new DefaultComboBoxModel();
        requisitioners.forEach(requisitioner -> {
            requisitionerModel.addElement(requisitioner.getFirstName() + " " + requisitioner.getLastName());
        });
        
        SwingUtilities.invokeLater(() -> {
            cbxProject.setModel(projectModel);
            cbxRequisitioner.setModel(requisitionerModel);
            cbxProject.setSelectedIndex(-1);
            cbxRequisitioner.setSelectedIndex(-1);
            GregorianCalendar cal = new GregorianCalendar();
            cal.setTime(new Date());
            cal.add(Calendar.DATE, -1);
            jdcDateFrom.setDate(cal.getTime());
            jdcDateTo.setDate(new Date());
        });
    }

    private void enableFields(boolean enability) {
        cbxProject.setEnabled(enability);
        cbxRequisitioner.setEnabled(enability);
        jdcDateFrom.setEnabled(enability);
        jdcDateTo.setEnabled(enability);
        btnSearch.setEnabled(enability);
    }

    private void reloadTable() {
        Vector<Vector> dataTable = new Vector<>();
        withdrawalReports.forEach(wr -> {
            Vector<String> row = new Vector<>();
            row.add(wr.getWithdrawalNo());
            row.add(wr.getWithdrawalDate().toString());
            row.add(wr.getTotalAmount().toString());
            try { row.add(wr.getProject().getProjectName()); } catch(Exception e) {e.printStackTrace();}
            User requestedBy = wr.getRequestedBy();
            row.add(requestedBy.getFirstName() + " " +  requestedBy.getLastName());
            dataTable.add(row);
        });
        DefaultTableModel withdrawalModel = new DefaultTableModel(dataTable, columns) {
            boolean[] canEdit = new boolean [] {
                false, false, false, false, false
            };
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        };
        tblWithdrawal.setModel(withdrawalModel);
        if (tblWithdrawal.getColumnModel().getColumnCount() > 0) {
            tblWithdrawal.getColumnModel().getColumn(0).setResizable(false);
            tblWithdrawal.getColumnModel().getColumn(0).setPreferredWidth(100);
            tblWithdrawal.getColumnModel().getColumn(1).setResizable(false);
            tblWithdrawal.getColumnModel().getColumn(1).setPreferredWidth(100);
            tblWithdrawal.getColumnModel().getColumn(2).setResizable(false);
            tblWithdrawal.getColumnModel().getColumn(2).setPreferredWidth(200);
            tblWithdrawal.getColumnModel().getColumn(3).setResizable(false);
            tblWithdrawal.getColumnModel().getColumn(3).setPreferredWidth(300);
            tblWithdrawal.getColumnModel().getColumn(4).setResizable(false);
        }
        
    }
