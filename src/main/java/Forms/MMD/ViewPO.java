    private void tblPurchaseOrdersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPurchaseOrdersMouseClicked
        SwingUtilities.invokeLater(() -> {
            if(evt.getClickCount()>1) {
                JTable source = (JTable)evt.getSource();
                int row = source.rowAtPoint( evt.getPoint());
                PurchaseOrder purchaseOrder = purchaseOrders.get(row);
                dispose();
                new ShowPurchaseOrder(this, purchaseOrder).setVisible(true);
            }
        });
        
    }//GEN-LAST:event_tblPurchaseOrdersMouseClicked

