    private void initializeData() {
        columns.add("PO No");
        columns.add("Project");
        columns.add("Department");
        columns.add("Supplier");
        columns.add("PO Date");
        columns.add("Delivery Date");
        columns.add("Total Amount");
        tblPurchaseOrders.setDefaultEditor(Object.class, null);
        departments = DepartmentService.findAll();
        AutoCompleteDecorator.decorate(cbxDepartment);
        DefaultComboBoxModel departmentModel = new DefaultComboBoxModel();
        departments.forEach(department -> {
            departmentModel.addElement(department.getName());
        });
        cbxDepartment.setModel(departmentModel);
        cbxDepartment.setSelectedIndex(-1);
        GregorianCalendar cal = new GregorianCalendar();
        cal.setTime(new Date());
        cal.add(Calendar.DATE, -1);
        jdcDateFrom.setDate(cal.getTime());
        jdcDateTo.setDate(new Date());
    }

    private void enableFields(boolean enability) {
        cbxDepartment.setEnabled(enability);
        jdcDateFrom.setEnabled(enability);
        jdcDateTo.setEnabled(enability);
        btnIncomplete.setEnabled(enability);
        btnComplete.setEnabled(enability);
        btnBoth.setEnabled(enability);
    }

    private void reloadTable() {
        Vector<Vector> dataTable = new Vector<>();
        purchaseOrders.forEach(purchaseOrder -> {
            Vector<String> row = new Vector<>();
            row.add(purchaseOrder.getPoNo());
            row.add(purchaseOrder.getProject().getProjectName());
            row.add(purchaseOrder.getProject().getDepartment().getName());
            row.add(purchaseOrder.getSupplier().getName());
            row.add(purchaseOrder.getPoDate().toString());
            row.add(purchaseOrder.getDeliveryDate().toString());
            String totalAmount = "";
            try {
                
                totalAmount = purchaseOrder.getTotalAmount().toString();
            } catch(Exception ex) {
                totalAmount = "0.00";
            }
            row.add(totalAmount);
            dataTable.add(row);
        });
        tableModel = new DefaultTableModel(dataTable, columns);
        tblPurchaseOrders.setModel(tableModel);
    }

    private void searchPO() {
        boolean statusComplete, statusIncomplete;
        if(btnStatus.isSelected(btnIncomplete.getModel())) {
            statusComplete = false;
            statusIncomplete = false;
        } else if(btnStatus.isSelected(btnComplete.getModel())) {
            statusComplete = true;
            statusIncomplete = true;
        } else {
            statusComplete = true;
            statusIncomplete = false;
        }
        Department department = (cbxDepartment.getSelectedIndex() > -1) ? departments.get(cbxDepartment.getSelectedIndex()) : null;
        purchaseOrders =  PurchaseOrderService.findBy(department, jdcDateFrom.getDate(), jdcDateTo.getDate(), statusComplete, statusIncomplete);
        reloadTable();
    }
